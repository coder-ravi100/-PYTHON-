#TYPE CONVERTION

#1.INT TO FLOAT
a = 10
b = float (a)
print(b + 2)
#---------------------------

#2.FLOAT TO INT 
a = 11.11
b = int (a)
print(b + 1)
#---------------------------

#3.STRING () TO INT ()
a = "10"
b = int(a) #STRING TO INT
print(b + 5,end = "\n") #OUTPUT :15
#---------------------------

#4.STRING TO FLOAT
a = "3.14"
b = float(a)
print(b + 1)  #output 4.14

